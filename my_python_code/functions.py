#add two numbers together
def addition_of_two(num1, num2):
    return (num1 + num2)

#add three numbers together
def addition_of_three(num1, num2, num3):
    return (num1 + num2 + num3)

#concatenate the names of a few of your closest friends
def closest_friends_names(name1, name2, name3):
    return f"{name1}{name2}{name3}"

#calculate the number of seconds in X weeks, where X is a number (like 3)
def number_of_seconds(Weeks):
    return Weeks * 60 * 60 * 24 * 7